function getLineByIndex(index) {
    return PAYLINES[index];
}

function generatePlateSymbolExtend(plateSymbol, gameId) {
    return Array.from({ length: 5 }, (_, row) =>
        Array.from({ length: 3 }, (_, col) =>
            buildSymbolImageUrl(gameId, plateSymbol[row].Col[col].Symbol)
        )
    );
}

/**
 * 根据列倍率生成对应的图片数组结构
 * @param {string} gameId
 * @param {number[]} wildColumnMult - 每列的倍率数组，例如 [0, 1, 12, 0, 123]
 * @returns {Array<Array<string>>} 返回二维数组，每一列对应一组图片
 */
function buildWildColumnImages(gameId, wildColumnMult) {
    return wildColumnMult.map(item =>
        item.Col.map(col => {
            if (col.JPState) {
                return {
                    img: buildSymbolImageUrlJP(gameId, col.JPState),
                    mul: col.JPMult,
                };
            }
            return {
                number: col.Number ? col.Number + "" : 0,
            };
        })
    );
}

function generateHighlightListFromWinning(winningCells) {
    const list = Array.from({ length: 5 }, () => []);
    winningCells.forEach(c => list[c.col].push(c.row));
    return list.map(arr => Array.from(new Set(arr)).sort((a, b) => a - b));
}

/**
 * 计算每列中奖符号在列中的具体行索引（左到右连续）
 * @param {Array} PlateSymbol 每列符号二维数组
 */
function validateLineOnPlate(plateSymbol, lineCells) {
    debugger
    // 原有的 WILD/普通符号连线逻辑
    const cellsWithId = lineCells.map(p => ({
        col: p.col,
        row: p.row,
        symbolId: plateSymbol[p.col].Col[p.row].Symbol,
    }));

    const matchedCells = [];
    let count = 0;
    // 找到第一个非 WILD/SCATTER 符号作为目标符号
    const targetCell = cellsWithId.find(c => WILD_ID !== c.symbolId);
    const targetId = targetCell ? targetCell.symbolId : WILD_ID;

    for (let i = 0; i < cellsWithId.length; i++) {
        const c = cellsWithId[i];
        if (targetId === c.symbolId || WILD_ID === c.symbolId) {
            matchedCells.push(c);
            count++;
        } else {
            if (count >= 3) break;
            count = 0;
            matchedCells.length = 0;
        }
    }

    return matchedCells.length >= 3 ? matchedCells : [];
}

function processFreeQueue(gameData, gameId) {
    const RoundQueue = gameData.RoundQueue || [];
    const convertedMainPlate = RoundQueue[0].MainPlateSymbol.map(item => ({
        Col: item.Col.map(value => ({
            Symbol: value,
        })),
    }));

    RoundQueue[0].FreePlateSymbol = convertedMainPlate;

    const plates = RoundQueue.map((roundData, index) => {
        let RoundWin = 0;
        const PlateSymbol = roundData.FreePlateSymbol;

        const PlateSymbolExtend = generatePlateSymbolExtend(PlateSymbol, gameId);

        let PlateSymbolNumImg = [];
        if (index !== 0) {
            PlateSymbolNumImg = buildWildColumnImages(gameId, PlateSymbol);
        }

        const AwardDataVec = (roundData.AwardDataVec || []).map((award, idx) => {
            const { Win, Symbol = 0, Line = 0 } = award;

            RoundWin += Win || 0;

            const line = getLineByIndex(Line);

            const awardsDetail = line ? validateLineOnPlate(PlateSymbol, line) : [];

            award.Line = Line + 1;

            return {
                ...award,
                uuid: uuid(),
                img: buildSymbolImageUrl(gameId, Symbol),
                list: generateHighlightListFromWinning(awardsDetail),
            };
        });

        return {
            ...roundData,
            uuid: uuid(),
            PlateSymbolNumImg,
            PlateSymbolExtend,
            RoundWin,
            AwardDataVec,
        };
    });

    if (plates.length) {
        plates.at(-1).TotalWin = gameData.TotalWin;
    }

    return plates;
}

// ==========================================
// 最终处理函数
// ==========================================
export function processGameData(gameData, gameId) {
    const rounds = processFreeQueue(gameData, gameId);

    return {
        ...gameData,
        uuid: uuid(),
        rounds,
    };
}
