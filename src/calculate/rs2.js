function getLineByIndex(index) {
    return PAYLINES[index];
}

function generateHighlightListFromWinning(winningCells) {
    const list = Array.from({ length: 5 }, () => []);
    winningCells.forEach(c => list[c.col].push(c.row));
    return list.map(arr => Array.from(new Set(arr)).sort((a, b) => a - b));
}

function generatePlateSymbolExtend(plateSymbol, gameId) {
    const reversedData = plateSymbol.map(item => ({
        Col: item.Col.slice().reverse(),
    }));

    const rowCount = reversedData.length;
    const colCount = reversedData[0].Col.length;

    return Array.from({ length: rowCount }, (_, row) =>
        Array.from({ length: colCount }, (_, col) =>
            buildSymbolImageUrl(gameId, reversedData[row].Col[col])
        )
    );
}

function generatePlateSymbolExtendArray(plateSymbol) {
    return plateSymbol.map(item => ({
        Col: item.Col.slice().reverse(),
    }));
}

function validateLineOnPlate(plateSymbol, lineCells) {
    const cellsWithId = lineCells.map(p => ({
        col: p.col,
        row: p.row,
        symbolId: plateSymbol[p.col].Col[p.row],
    }));

    // 找到第一个非 WILD/SCATTER 符号作为目标符号
    const targetCell = cellsWithId.find(c => c.symbolId !== WILD_ID);
    const targetId = targetCell ? targetCell.symbolId : WILD_ID;

    let count = 0;
    const matchedCells = [];

    for (let i = 0; i < cellsWithId.length; i++) {
        const c = cellsWithId[i];
        if (c.symbolId === targetId || c.symbolId === WILD_ID) {
            matchedCells.push(c);
            count++;
        } else {
            // 遇到不匹配就中断，如果当前累计>=3，直接返回中奖
            if (count >= 3) break;
            count = 0;
            matchedCells.length = 0;
        }
    }

    return {
        ok: matchedCells.length >= 3,
        cells: matchedCells.length >= 3 ? matchedCells : [],
    };
}

// ==========================================
// MainInfo + FreeQueue 处理
// ==========================================
function processMainInfo(RoundQueue, gameId) {
    return RoundQueue.map((roundData, roundIndex) => {
        let isFrist = false;
        let RoundWin = 0;
        const ComboStageData = roundData.ComboStageData || [];

        if (roundIndex === 0) {
            isFrist = true;
        }

        const plates = ComboStageData.map((plate, plateIndex) => {
            const PlateSymbolExtend = generatePlateSymbolExtend(plate.ComboStageSymbol, gameId);
            const PlateSymbolExtendArray = generatePlateSymbolExtendArray(plate.ComboStageSymbol);

            const AwardDataVec = (plate.AwardDataVec || []).map(award => {
                const { Line = 0, Money } = award;

                const line = getLineByIndex(Line);

                const awardsDetail = line
                    ? validateLineOnPlate(PlateSymbolExtendArray, line).cells
                    : [];

                RoundWin += Money || 0;

                award.Index = Line + 1;

                return {
                    ...award,
                    uuid: uuid(),
                    list: generateHighlightListFromWinning(awardsDetail),
                };
            });

            return {
                ...plate,
                uuid: uuid(),
                RoundWin,
                PlateSymbolExtend,
                AwardDataVec,
            };
        });
        return {
            ...roundData,
            isFrist,
            uuid: uuid(),
            plates,
        };
    });
}

function processFreeQueue(freeInfo, gameId) {
    return freeInfo.map(item => {
        const { Symbol, HealthPoint, Result } = item;

        const record = [
            {
                img: buildSymbolImageUrl(gameId, Symbol),
                num: HealthPoint,
            },
            ...(Result && HealthPoint !== Result
                ? [
                      {
                          img: buildSymbolImageUrl(gameId, 14),
                          num: HealthPoint - Result,
                      },
                  ]
                : []),
        ];

        return {
            ...item,
            uuid: uuid(),
            record,
        };
    });
}

// ==========================================
// 最终处理函数
// ==========================================
export function processGameData(gameData, gameId) {
    let rounds = gameData?.RoundQueue ? processMainInfo(gameData.RoundQueue, gameId) : null;

    let frees = gameData?.BonusInfo ? processFreeQueue(gameData.BonusInfo, gameId) : null;

    if (gameData.BonusInfo?.length > 1) {
        gameData.isFree = true;
    } else {
        gameData.isFree = false;
    }

    return {
        ...gameData,
        uuid: uuid(),
        rounds,
        frees,
    };
}
